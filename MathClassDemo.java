// ex of math class
class MathClassDemo
{
	public static void main(String args[])
	{
	System.out.println(Math.pow(2,3));
	System.out.println(Math.sqrt(25));
	System.out.println(Math.log(2)); //base e
	System.out.println(Math.log10(10)); //base 10
	System.out.println(Math.floor(42.4));
	System.out.println(Math.ceil(42.4));
	System.out.println(Math.exp(2));
	System.out.println(Math.max(20,40));
	System.out.println(Math.min(20,40));
	System.out.println(Math.abs(-2));
	}
}